# Константы для направлений и действий
FOOD, NOFOOD = 0, 1
MOVE, RIGHT, LEFT, NO = 0, 1, 2, 3
NORTH, EAST, SOUTH, WEST = 0, 1, 2, 3

MAX_STATE = 4
last_signal = 2
last_action = 4


# Класс для состояния и действия
class SA:
    def __init__(self, state, action):
        self.state = state
        self.action = action


# Инициализация таблицы состояний (STT) с начальными значениями
# STT = [
#     [SA(3, RIGHT), SA(0, LEFT)],
#     [SA(0, RIGHT), SA(2, MOVE)],
#     [SA(2, MOVE), SA(2, RIGHT)],
#     [SA(3, MOVE), SA(2, LEFT)]
# ]

STT = [
    [SA(1, RIGHT), SA(0, LEFT)],
    [SA(0, RIGHT), SA(1, MOVE)],
    [SA(2, MOVE), SA(0, RIGHT)],
    [SA(1, MOVE), SA(3, LEFT)]
]


# Вспомогательные функции для вывода
def direction_name(d):
    return ["NORTH", "EAST", "SOUTH", "WEST"][d]

def action_name(a):
    return ["MOVE", "RIGHT", "LEFT", "NO"][a]


# Симуляция конечного автомата
def fsm_steps(max_steps, STT):
    state = 0
    score = 0
    x, y = 0, 0
    d = EAST
    xdir = [1, 0, -1, 0]
    ydir = [0, 1, 0, -1]
    Tor_copy = copy.deepcopy(Tor)
    for _ in range(max_steps):
        xs, ys = (x + xdir[d]) % 32, (y + ydir[d]) % 32
        signal = FOOD if Tor_copy[ys][xs] else NOFOOD
        sa = STT[state][signal]
        if sa.action == MOVE:
            x, y = (x + xdir[d]) % 32, (y + ydir[d]) % 32
            if Tor_copy[y][x] == 1:
                score += 1
                Tor_copy[y][x] = 0
        elif sa.action == LEFT:
            d = (d - 1) % 4
        elif sa.action == RIGHT:
            d = (d + 1) % 4

        state = sa.state
    return score


# Генерация новой стратегии (состояний и действий)
def generate_new_stt(STT):
    new_STT = copy.deepcopy(STT)
    state = random.randint(0, MAX_STATE - 1)
    signal = random.randint(0, last_signal - 1)
    new_STT[state][signal] = SA(random.randint(0, MAX_STATE - 1), random.randint(0, last_action - 1))
    return new_STT


# Функция для вычисления вероятности принятия нового решения
def acceptance_probability(current_score, new_score, T):
    if new_score > current_score:
        return 1.0
    return math.exp((new_score - current_score) / T)


# Метод отжига
def simulated_annealing(start_T, end_T, alpha, max_steps):
    current_STT = copy.deepcopy(STT)
    best_STT = copy.deepcopy(STT)
    best_score = fsm_steps(max_steps, best_STT)
    T = start_T

    while T > end_T:
        new_STT = generate_new_stt(current_STT)
        new_score = fsm_steps(max_steps, new_STT)

        if acceptance_probability(best_score, new_score, T) > random.random():
            current_STT = new_STT
            if new_score > best_score:
                best_score = new_score
                best_STT = copy.deepcopy(new_STT)

        T *= alpha

    return best_STT, best_score


# Главная функция
def main():
    random.seed()
    start_T = 1000.0
    end_T = 0.01
    alpha = 0.99
    max_steps = 200

    optimized_STT, final_score = simulated_annealing(start_T, end_T, alpha, max_steps)
    print(f"Final score with optimized strategy: {final_score}")
    for i, states in enumerate(optimized_STT):
        print(f"State {i}:")
        for j, sa in enumerate(states):
            print(f" Signal {j} -> State {sa.state}, Action {action_name(sa.action)}")


main()
